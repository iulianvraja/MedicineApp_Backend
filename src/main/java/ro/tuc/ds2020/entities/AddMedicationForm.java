package ro.tuc.ds2020.entities;

public class AddMedicationForm {

    private String numem;
    private String numep;
    private String interval;

    public AddMedicationForm() {
    }

    public String getNumem() {
        return numem;
    }

    public void setNumem(String numem) {
        this.numem = numem;
    }

    public String getNumep() {
        return numep;
    }

    public void setNumep(String numep) {
        this.numep = numep;
    }

    public String getInterval() {
        return interval;
    }

    public void setInterval(String interval) {
        this.interval = interval;
    }
}
